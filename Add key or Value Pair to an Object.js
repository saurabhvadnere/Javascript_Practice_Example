// program to add a key/value pair to an object

// Example 1: Using Dot Notation

const person = {
    name: 'Marena',
    age: 22,
    gender: 'female'
}

// add a key/value pair
person.height = 5.4;

console.log(person);

// Example 2: Using Square Bracket Notation

const persons = {
    name: 'Saurabh',
    age: 24,
    gender: 'male'
}

// add a key/value pair
persons['height'] = 5.8;

console.log(persons);